package com.hp.octane.integrations.services.pullrequests.github.pojo;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

@JsonIgnoreProperties(ignoreUnknown = true)
public class PullRequest extends Entity {
    private int number;
    private String html_url;
    private String commits_url;
    private String state;
    private String title;
    private String body;

    private String created_at;
    private String updated_at;
    private String closed_at;
    private String merged_at;

    private PullRequestUser user;

    private PullRequestRepo base;
    private PullRequestRepo head;

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getCreated_at() {
        return created_at;
    }

    public void setCreated_at(String created_at) {
        this.created_at = created_at;
    }

    public String getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(String updated_at) {
        this.updated_at = updated_at;
    }

    public String getClosed_at() {
        return closed_at;
    }

    public void setClosed_at(String closed_at) {
        this.closed_at = closed_at;
    }

    public String getMerged_at() {
        return merged_at;
    }

    public void setMerged_at(String merged_at) {
        this.merged_at = merged_at;
    }

    public PullRequestUser getUser() {
        return user;
    }

    public void setUser(PullRequestUser user) {
        this.user = user;
    }

    public PullRequestRepo getBase() {
        return base;
    }

    public void setBase(PullRequestRepo base) {
        this.base = base;
    }

    public PullRequestRepo getHead() {
        return head;
    }

    public void setHead(PullRequestRepo head) {
        this.head = head;
    }

    public String getCommits_url() {
        return commits_url;
    }

    public void setCommits_url(String commits_url) {
        this.commits_url = commits_url;
    }

    public String getHtml_url() {
        return html_url;
    }

    public void setHtml_url(String html_url) {
        this.html_url = html_url;
    }
}
